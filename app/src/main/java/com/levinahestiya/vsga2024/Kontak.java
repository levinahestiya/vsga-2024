package com.levinahestiya.vsga2024;

public class Kontak {
    private String nama;
    private String alamat;

    public Kontak(String nama, String alamat) {
        this.nama = nama;
        this.alamat = alamat;
    }

    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }
}